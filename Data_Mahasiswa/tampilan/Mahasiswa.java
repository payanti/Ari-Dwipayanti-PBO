    private void tabelMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMahasiswaMouseClicked
        // TODO add your handling code here:
        int baris = tabelMahasiswa.getSelectedRow();  // Dapatkan baris yang dipilih
    if (baris != -1) {
        // Ambil data dari kolom yang dipilih dan tampilkan di JTextField
        String nim = tabelMahasiswa.getValueAt(baris, 0).toString();
        String nama = tabelMahasiswa.getValueAt(baris, 1).toString();
        String alamat = tabelMahasiswa.getValueAt(baris, 4).toString();
        String jenis_kelamin = tabelMahasiswa.getValueAt(baris, 2).toString();
        String phone = tabelMahasiswa.getValueAt(baris, 3).toString();
        String prodi = tabelMahasiswa.getValueAt(baris, 5).toString();
        // Tampilkan data di JTextField
        textnim.setText(nim);
        textnama.setText(nama);      
        // Pilih jenis kelamin berdasarkan data
        if (jenis_kelamin.equals("Pria")) {
        rbLaki.setSelected(true);
        } else if (jenis_kelamin.equals("Wanita")) {
        rbPerempuan.setSelected(true);
}

        textphone.setText(phone);
        textalamat.setText(alamat);
        textprodi.setText(prodi);
    }

    }//GEN-LAST:event_tabelMahasiswaMouseClicked

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String nim = textnim.getText(); String nama = textnama.getText(); String jenis_kelamin = ""; // bisa diambil dari radio button
        String phone = textphone.getText(); String alamat = textalamat.getText(); String prodi = textprodi.getText();
        if (rbLaki.isSelected()) { jenis_kelamin = "Pria"; } 
        else if (rbPerempuan.isSelected()) { jenis_kelamin = "Wanita"; }

        // Buat query SQL untuk menambah data
        String sql = "INSERT INTO mahasiswa (nim, nama, alamat, jenis_kelamin, phone, prodi) VALUES (?, ?, ?, ?, ?, ?)";
    try {
        PreparedStatement stmt = (PreparedStatement) conn.prepareStatement(sql);
        // Set parameter untuk query
        stmt.setString(1, nim);
        stmt.setString(2, nama);
        stmt.setString(3, alamat);
        stmt.setString(4, jenis_kelamin);
        stmt.setString(5, phone);
        stmt.setString(6, prodi);
        // Eksekusi query
        int rowsInserted = stmt.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
        }
        tampilkanData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menambahkan data: " + e.getMessage());
    }    

    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        String nim = textnim.getText();    
    if (nim.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Masukkan NIM untuk menghapus data.");
        return;
    }
    // Query SQL untuk menghapus data berdasarkan NIM
    String sql = "DELETE FROM mahasiswa WHERE nim = ?";
    try {
            PreparedStatement stmt = (PreparedStatement) conn.prepareStatement(sql);
             stmt.setString(1, nim);
        // Eksekusi query
        int rowsDeleted = stmt.executeUpdate();
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            bersihkanTextbox(); // Bersihkan textbox setelah hapus
            tampilkanData();    // Tampilkan data terupdate di tabel
        } else {
            JOptionPane.showMessageDialog(null, "Data dengan NIM " + nim + " tidak ditemukan.");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Gagal menghapus data: " + ex.getMessage());
    }

    }//GEN-LAST:event_btnhapusActionPerformed

