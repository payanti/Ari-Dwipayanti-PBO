    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
            String nim = textnim.getText(); 
    String nama = textnama.getText(); 
    String jenis_kelamin = "";
    String phone = textphone.getText(); 
    String alamat = textalamat.getText(); 
    String prodi = textprodi.getText();

    if (rbLaki.isSelected()) { jenis_kelamin = "Pria"; } 
    else if (rbPerempuan.isSelected()) { jenis_kelamin = "Wanita"; }

    String sql = "UPDATE mahasiswa SET nama = ?, jenis_kelamin = ?, phone = ?, alamat = ?, prodi = ? WHERE nim = ?";

    try {
        // Pastikan koneksi database sudah dibuat sebelumnya
        PreparedStatement stmt = conn.prepareStatement(sql);
        
        // Perhatikan urutan parameter sesuai query SQL
        stmt.setString(1, nama);
        stmt.setString(2, jenis_kelamin);
        stmt.setString(3, phone);
        stmt.setString(4, alamat);
        stmt.setString(5, prodi);
        stmt.setString(6, nim); // nim di bagian WHERE
 

        int rowsUpdated = stmt.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
        } else {
            JOptionPane.showMessageDialog(null, "NIM tidak ditemukan!");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal mengubah data: " + e.getMessage());
    }
    }//GEN-LAST:event_btnubahActionPerformed

