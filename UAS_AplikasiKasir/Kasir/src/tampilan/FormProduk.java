    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
         String nama = txtproduk.getText();
         String harga = txtharga.getText();
         String stok = txtStok.getText();

    try {
        // 1. Koneksi ke database
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kasir", "root", ""); // ganti sesuai koneksi database kamu

        // 2. Query insert data
        String query = "INSERT INTO produk_new (id_produk, nama_produk, harga, stok) VALUES ('" + id + "', '" + nama + "', '" + harga + "', '" + stok + "')";

        Statement stmt = con.createStatement();
        stmt.executeUpdate(query);

        // 3. Tampilkan juga ke tabel (opsional)
        DefaultTableModel model = (DefaultTableModel) tabelProduk.getModel();
        model.addRow(new Object[]{id, nama, harga, stok});

        // 4. Bersihkan form
        btnbersihkanActionPerformed(null);

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database.");

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
    }
         
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int row = tabelProduk.getSelectedRow();

    if (row >= 0) {
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                String id = tabelProduk.getValueAt(row, 0).toString();

                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kasir", "root", "");
                String query = "DELETE FROM produk_new WHERE id_produk=?";
                PreparedStatement stmt = con.prepareStatement(query);
                stmt.setString(1, id);
                int deleted = stmt.executeUpdate();

                if (deleted > 0) {
                    DefaultTableModel model = (DefaultTableModel) tabelProduk.getModel();
                    model.removeRow(row);
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal dihapus dari database.");
                }

                stmt.close();
                con.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus");
    }
       
    }//GEN-LAST:event_btnhapusActionPerformed

