private void updateRowInTable(String id, String nama, String harga, String stok) {
    DefaultTableModel model = (DefaultTableModel) tabelProduk.getModel();

    for (int i = 0; i < model.getRowCount(); i++) {
        if (model.getValueAt(i, 0).toString().equals(id)) {
            model.setValueAt(nama, i, 1);
            model.setValueAt(harga, i, 2);
            model.setValueAt(stok, i, 3);
            break;
        }
    }
}
