    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelProduk.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih salah satu baris di tabel terlebih dahulu.");
        return;
    }

    String id = txtID.getText();  // Dari field, karena sudah diisi otomatis
    String nama = txtproduk.getText();
    String harga = txtharga.getText();
    String stok = txtStok.getText();

    if (nama.isEmpty() || harga.isEmpty() || stok.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silakan lengkapi semua field sebelum menyimpan.");
        return;
    }

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kasir", "root", "");

        String updateQuery = "UPDATE produk_new SET nama_produk=?, harga=?, stok=? WHERE id_produk=?";
        PreparedStatement stmt = con.prepareStatement(updateQuery);  // âœ… TANPA CASTING
        stmt.setString(1, nama);
        stmt.setString(2, harga);
        stmt.setString(3, stok);
        stmt.setString(4, id);

        int rowsUpdated = stmt.executeUpdate();

        if (rowsUpdated > 0) {
            // Perbarui tampilan tabel juga
            DefaultTableModel model = (DefaultTableModel) tabelProduk.getModel();
            model.setValueAt(nama, selectedRow, 1);
            model.setValueAt(harga, selectedRow, 2);
            model.setValueAt(stok, selectedRow, 3);

            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.");
            resetForm();// mengosongkan input field setelah update
            tampilkanData(); // refresh isi tabel dari database
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error saat update: " + e.getMessage());
    }
    }//GEN-LAST:event_btnubahActionPerformed

