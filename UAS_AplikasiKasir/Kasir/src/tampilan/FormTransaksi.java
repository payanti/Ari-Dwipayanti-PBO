    private void txtCetakStrukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCetakStrukActionPerformed
        // TODO add your handling code here: 
         DefaultTableModel model = (DefaultTableModel) tabelTransaksi.getModel();
    if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Tidak ada transaksi untuk disimpan.");
        return;
    }

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kasir", "root", "");

        System.out.println("===== STRUK PEMBELIAN =====");
        int totalKeseluruhan = 0;

        // Hitung total keseluruhan dulu
        for (int i = 0; i < model.getRowCount(); i++) {
            int harga = Integer.parseInt(model.getValueAt(i, 2).toString());
            int jumlah = Integer.parseInt(model.getValueAt(i, 3).toString());
            totalKeseluruhan += harga * jumlah;
        }

        // Ambil jumlah bayar dan hitung kembalian
        int bayar = Integer.parseInt(txtbayar.getText());
        int kembalian = bayar - totalKeseluruhan;

        // Simpan data dan cetak struk
        for (int i = 0; i < model.getRowCount(); i++) {
            String id = model.getValueAt(i, 0).toString();
            String nama = model.getValueAt(i, 1).toString();
            int harga = Integer.parseInt(model.getValueAt(i, 2).toString());
            int jumlah = Integer.parseInt(model.getValueAt(i, 3).toString());
            String tanggal = model.getValueAt(i, 4).toString();

            int subtotal = harga * jumlah;

            // Cetak per item
            System.out.println("Tanggal     : " + tanggal);
            System.out.println("ID Produk   : " + id);
            System.out.println("Nama Produk : " + nama);
            System.out.println("Harga       : " + harga);
            System.out.println("Jumlah      : " + jumlah);
            System.out.println("Subtotal    : " + subtotal);
            System.out.println("---------------------------");

            // Simpan ke database
            String insertQuery = "INSERT INTO transaksi_new (id_produk, nama_produk, harga, jumlah, tanggal, total, jumlah_bayar, kembalian) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(insertQuery);
            ps.setString(1, id);
            ps.setString(2, nama);
            ps.setInt(3, harga);
            ps.setInt(4, jumlah);
            ps.setString(5, tanggal);
            ps.setInt(6, subtotal); // total per item
            ps.setInt(7, bayar);
            ps.setInt(8, kembalian);
            ps.executeUpdate();
        }

        // Cetak total akhir dan kembalian
        System.out.println("Total Keseluruhan : " + totalKeseluruhan);
        System.out.println("Jumlah Dibayar    : " + bayar);
        System.out.println("Kembalian         : " + kembalian);
        System.out.println("===========================");
        System.out.println("Terima kasih telah berbelanja!\n");

        JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan dan struk dicetak.");

        // Bersihkan tabel dan form
        model.setRowCount(0);
        resetForm();

    } catch (SQLException | NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan transaksi: " + e.getMessage());
    }
        
    }//GEN-LAST:event_txtCetakStrukActionPerformed

