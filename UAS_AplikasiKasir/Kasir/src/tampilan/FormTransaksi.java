    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
    String nama = txtproduk.getText();
    String harga = txtharga.getText();
    String jumlahStr = txtjumlah.getText();
    java.util.Date utilDate = jDateChooser1.getDate();   // Tanggal

    // Cek jika tanggal kosong
    if (utilDate == null) {
        JOptionPane.showMessageDialog(this, "Tanggal tidak boleh kosong.");
        return;
    }

    // Konversi ke SQL date
    java.sql.Date tanggal = new java.sql.Date(utilDate.getTime());

    // Validasi field lainnya
    if (id.isEmpty() || nama.isEmpty() || harga.isEmpty() || jumlahStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Lengkapi semua field terlebih dahulu.");
        return;
    }

    int jumlah = Integer.parseInt(jumlahStr);
    int hargaInt = Integer.parseInt(harga);
    int total = jumlah * hargaInt;
    
    model.addRow(new Object[]{id, nama, harga, jumlah, tanggal});

    // Hitung total semua item
    int totalBayar = 0;
    for (int i = 0; i < model.getRowCount(); i++) {
        int h = Integer.parseInt(model.getValueAt(i, 2).toString());
        int j = Integer.parseInt(model.getValueAt(i, 3).toString());
        totalBayar += h * j;
    }
    txttotal.setText(String.valueOf(totalBayar));
    }//GEN-LAST:event_btnTambahActionPerformed

