    private void txtIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDKeyPressed
        // TODO add your handling code here:
        String id = txtID.getText();
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kasir", "root", "");
        String query = "SELECT nama_produk, harga FROM produk_new WHERE id_produk = ?";
        PreparedStatement ps = con.prepareStatement(query);
        ps.setString(1, id);
        ResultSet rs = (ResultSet) ps.executeQuery();

        if (rs.next()) {
            txtproduk.setText(rs.getString("nama_produk"));
            txtharga.setText(rs.getString("harga"));
        } else {
            txtproduk.setText("");
            txtharga.setText("");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal mengambil data produk: " + e.getMessage());
    }
    }//GEN-LAST:event_txtIDKeyPressed

